    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here:
        if (email.getText().isEmpty() || password.getPassword().length == 0) {
        JOptionPane.showMessageDialog(this, "Both email and password fields must be filled!");
        return;
    }

    String mysqlAdmin = "select email, password from user where email = ? and password = ?";

    try {
        pst = con.prepareStatement(mysqlAdmin);
        pst.setString(1, email.getText());
        pst.setString(2, new String(password.getPassword()));
        rst = pst.executeQuery();

        if (rst.next()) {
            String loggedInEmail = rst.getString("email");

            if (email.getText().equals(loggedInEmail)
                    && new String(password.getPassword()).equals(rst.getString("password"))) {

                model_user.setEmail(loggedInEmail); // Set email in LoginSession
                this.dispose();
                transaksiframe tr = new transaksiframe();
                tr.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Email or Password is wrong!");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    } finally {
        try {
            if (rst != null) rst.close();
            if (pst != null) pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    }//GEN-LAST:event_loginbtnActionPerformed

